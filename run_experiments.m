function run_experiments()

  rcnn.name = 'rcnn' ;
  rcnn.opts = {...
    'type', 'rcnn', ...
    'model', 'data/ft-models/cnn-train-m-f2/cars-seed-01/final-model.mat', ...
    'layer', 19} ;

  rcnnvd.name = 'rcnnvd' ;
  rcnnvd.opts = {...
    'type', 'rcnn', ...
    'model', 'data/ft-models/cnn-train-vd-f2/cars-seed-01/final-model.mat', ...
    'layer', 35} ;

  dcnn.name = 'dcnn' ;
  dcnn.opts = {...
    'type', 'dcnn', ...
    'model', 'data/ft-models/cnn-train-m-f2/cars-seed-01/final-model.mat', ...
    'layer', 14, ...
    'numWords', 64} ;

  dcnnvd.name = 'dcnnvd' ;
  dcnnvd.opts = {...
    'type', 'dcnn', ...
    'model', 'data/ft-models/cnn-train-vd-f2/cars-seed-01/final-model.mat', ...
    'layer', 30, ...
    'numWords', 64} ;

  dsift.name = 'dsift' ;
  dsift.opts = {...
    'type', 'dsift', ...
    'numWords', 256, ...
    'numPcaDimensions', 80} ;

  bcnnmm.name = 'bcnnmm' ;
  bcnnmm.opts = {...
    'type', 'bcnn', ...
    'modela', 'data/ft-models/bcnn-train-mm-f2/cars-seed-01/fine-tuned-model/final-model.mat', ...
    'layera', 14,...
    'modelb', 'data/ft-models/bcnn-train-mm-f2/cars-seed-01/fine-tuned-model/final-model.mat', ...
    'layerb', 14,...
    'normalization', 'sqrt_L2'
    } ;

  bcnnvdm.name = 'bcnnvdm' ;
  bcnnvdm.opts = {...
    'type', 'bcnn', ...
    'modela', 'data/ft-models/bcnn-train-dm-f2/cars-seed-01/fine-tuned-model/final-model-neta.mat', ...
    'layera', 30,...
    'modelb', 'data/ft-models/bcnn-train-dm-f2/cars-seed-01/fine-tuned-model/final-model-netb.mat', ...
    'layerb', 14,...    
    } ;

  bcnnvdvd.name = 'bcnnvdvd' ;
  bcnnvdvd.opts = {...
    'type', 'bcnn', ...
    'modela', 'data/ft-models/bcnn-train-dd-f2/cars-seed-01/fine-tuned-model/final-model.mat', ...
    'layera', 30,...
    'modelb', 'data/ft-models/bcnn-train-dd-f2/cars-seed-01/fine-tuned-model/final-model.mat', ...
    'layerb', 30,...
    };
  
   setupNameList = {'rcnn', 'rcnnvd', 'dcnn', 'dcnnvd', 'bcnnmm', 'bcnnvdvd', 'bcnnvdm'};
   encoderList = {{rcnn}, {rcnnvd}, {dcnn}, {dcnnvd}, {bcnnmm}, {bcnnvdvd}, {bcnnvdm}};
   datasetList = {{'cars', 1}};
%{
  setupNameList = {'rcnn', 'rcnnvd', 'dcnn', 'dcnnvd', 'dsift'};
  encoderList = {{rcnn}, {rcnnvd}, {dcnn}, {dcnnvd}, {dsift}};
  datasetList = {{'cub', 1}};
%}

  for ii = 1 : numel(datasetList)
    dataset = datasetList{ii} ;
    if iscell(dataset)
      numSplits = dataset{2} ;
      dataset = dataset{1} ;
    else
      numSplits = 1 ;
    end
    for jj = 1 : numSplits
      for ee = 1: numel(encoderList)
        model_train(...
          'dataset', dataset, ...
          'seed', jj, ...
          'encoders', encoderList{ee}, ...
          'prefix', 'results-ft-f2', ...
          'suffix', setupNameList{ee}, ...
          'printDatasetInfo', ee == 1, ...
          'useGpu', 1, ...
          'dataAugmentation', 'f2') ;
      end
    end
  end
end
